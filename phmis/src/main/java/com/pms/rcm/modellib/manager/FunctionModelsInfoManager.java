    public List<FunctionModelsInfo> findPage(FunctionModelsInfo entitySearch, Page page,
			boolean needSearchCount) throws BusinessException {
		StringBuffer sb = new StringBuffer();		
		sb.append("from FunctionModelsInfo w where 1=1 ");   
		 
		if(StrUtil.isNotEmpty(entitySearch.getFunctionModelName())){
			sb.append(" and w.functionModelName='" + entitySearch.getFunctionModelName() + "'");
		}
		if(StrUtil.isNotEmpty(entitySearch.getPhmfunctionTypeId())){
			sb.append(" and w.phmfunctionTypeId='" + entitySearch.getPhmfunctionTypeId() + "'");
		}
		if(StrUtil.isNotEmpty(entitySearch.getModelUrl())){
			sb.append(" and w.modelUrl='" + entitySearch.getModelUrl() + "'");
		}
		if(StrUtil.isNotEmpty(entitySearch.getModelDescription())){
			sb.append(" and w.modelDescription='" + entitySearch.getModelDescription() + "'");
		}

		String hql = sb.toString();//this.baseDao.buildHql(entitySearch, "w", sb.toString());		
		return super.findPage(hql, page, needSearchCount);
	}
	
