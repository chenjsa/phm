    public List<AdminInfo> findPage(AdminInfo entitySearch, Page page,
			boolean needSearchCount) throws BusinessException {
		StringBuffer sb = new StringBuffer();		
		sb.append("from AdminInfo w where 1=1 ");   
		 
		if(StrUtil.isNotEmpty(entitySearch.getAdminId())){
			sb.append(" and w.adminId='" + entitySearch.getAdminId() + "'");
		}
		if(StrUtil.isNotEmpty(entitySearch.getAdminPassword())){
			sb.append(" and w.adminPassword='" + entitySearch.getAdminPassword() + "'");
		}
		if(StrUtil.isNotEmpty(entitySearch.getTelephone())){
			sb.append(" and w.telephone='" + entitySearch.getTelephone() + "'");
		}

		String hql = sb.toString();//this.baseDao.buildHql(entitySearch, "w", sb.toString());		
		return super.findPage(hql, page, needSearchCount);
	}
	
